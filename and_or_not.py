# and keyword
a = [1]
b = [2]

# it will return b
# when both are true 2nd value is returned
# when both are false or 1st is false then it will return first
print(a and b)

# or keyword
# when both are true it will return the first value
# when one is true and other is false it will return true value
print(a or b)


# not returns the opposite of the value
